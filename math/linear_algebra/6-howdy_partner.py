#!/usr/bin/env python3
"""
Module that provides a function to concatenate two arrays.
"""


def cat_arrays(arr1, arr2):
    """
    Concatenates two arrays.

    Args:
        arr1 (list): First list of ints/floats.
        arr2 (list): Second list of ints/floats.

    Returns:
        list: A new list containing elements of arr1 followed by arr2.
    """
    return arr1 + arr2
